class Switch_Example
{
	public static void main(String[] args)
	{
		String ch="hy";
		switch (ch)
		{
			case "male" :  System.out.println(" Male ");
			break;
			
			case "female" : System.out.println(" Female ");
			break;
			
			default : System.out.println(" default ");
		}
	}
}
			